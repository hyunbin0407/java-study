package Day08;

public class Method2 {
    static void main() {
        printHeader();
        System.out.println("프로그램이 동작합니다.");
        printFooter();
    }

    static void printHeader() {
        System.out.println("= 프로그램을 시작합니다 =");
        return;
    }

    static void printFooter() {
        System.out.println("= 프로그램을 종료합니다 =");

    }
}
